# 고양이

# 문제
# 아래 예제와 같이 고양이를 출력하시오.

# 입력
# 없음.

# 출력
# 고양이를 출력한다.

# 예제 입력 1 
# 예제 출력 1 
# \    /\
#  )  ( ')
# (  /  )
#  \(__)|


print('\\    /\\')
print(" )  ( ')")
print('(  /  )')
print(' \(__)|')


# \ 부분에서 오류가 생겼다.
# \는 특별한 의미를 가지고 있기에 
# \뒤에 아무것도 없거나, 이해할 수 없는 문자가 오면 오류가 발생한다.

# \\를 두 번 쓰면 파이썬은 역슬래시 하나로 이해해서 출력해준다.

# 두번째 줄에 눈으로 ' 모양이 잡고 있어서 이 줄에는 ""를 이용해 출력
# 다른 줄 처럼 ' 를 이용해서 출력하려 하면 오류 발생

