
# 백준 1000번 : A+B
# 문제
# 두 정수 A와 B를 입력받은 다음, A+B를 출력하는 프로그램을 작성하시오.

# 입력
# 첫째 줄에 A와 B가 주어진다. (0 < A, B < 10)

# 출력
# 첫째 줄에 A+B를 출력한다

# 예제 입력 1
# 1 2
# 예제 출력 1
# 3


# 정답


A, B = input().split()	
print(int(A)+int(B))	

#or

a, b = map(int, input().split())
print(a+b)



# 풀이

A, B = input().split()
# 네가 키보드로 한 줄 입력하면 (예: 10 20),
# 그걸 띄어쓰기(공백) 기준으로 쪼개서 ("10", "20")
# 각각 A와 B 변수에 '글자(문자열)' 상태 그대로 넣어줘.

print(int(A)+int(B))
# A와 B는 아직 글자니까, 계산하려면 int()로 숫자로 바꿔줘야 해.
# int(A)는 A를 숫자로, int(B)는 B를 숫자로 바꾸는 거야.
# 그렇게 바꾼 두 숫자를 더해서 결과(30)를 보여줘!
# 결론: 한 줄에 여러 글자(숫자)를 입력받아서 변수에 넣고, 필요할 때 직접 숫자로 바꿔서 계산하는 방법!


# 이 코드는 백준 같은 온라인 저지에서 문제를 풀 때, 
# 여러 개의 숫자(또는 문자)를 한 줄에 입력받아서 각각 변수에 저장할 때 주로 사용
a, b = map(int, input().split())



# map() 으로 리스트 안에 int 를 각 각 적용해줌
# input() 사용자에게 입력 값 받기
#.split() 여기서는 () 안이 공백이기 때문에 스페이스 한 칸을 기준으로 문자열을 나눔






