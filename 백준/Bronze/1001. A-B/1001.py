

# 문제
# 두 정수 A와 B를 입력받은 다음, A-B를 출력하는 프로그램을 작성하시오.

# 입력
# 첫째 줄에 A와 B가 주어진다. (0 < A, B < 10)

# 출력
# 첫째 줄에 A-B를 출력한다.

# 예제 입력 1 
# 3 2
# 예제 출력 1 
# 1



a, b = map(int(), input().split()) 
print(a-b)


# int 정수로 바꿔줌
# input 사용자의 값을 받아 입력해줌
# split 사용자가 입력한 내용을 공백 기준으로 나눠서 리스트로 만들어줌

















