# 꼬마 정민 



# 문제
# 꼬마 정민이는 이제 A + B 정도는 쉽게 계산할 수 있다. 
# 이제 A + B + C를 계산할 차례이다!

# 입력
# 첫 번째 줄에 A, B, C (1 ≤ A, B, C ≤ 1012)이 공백을 사이에 두고 주어진다.

# 출력
# A+B+C의 값을 출력한다.

# 예제 입력 1 
# 77 77 7777
# 예제 출력 1 
# 7931


A , B , C = map(int,input().split())
print(A + B + C)


# map는 리스트의 각 요소를 하나씩 꺼내준다.
# int는 map에서 꺼내준 요소를 정수로 바꿔주고
# input는 사용자에게 3개의 값을 입력받게 해줘
# spilt는 입력받은 문자열을 공백 기준으로 잘라 리스트로 만들어줌
# 
# 이제 나머지 3개를 더한 걸 출력해주면 끝!  












